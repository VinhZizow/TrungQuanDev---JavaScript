// Bitwise Operator: tính toán nhị phân
// Bitwise AND
let a = 10;
let b = 5;
console.log(a & b);

// Bitwise OR
let c = 10;
let d = 5;
console.log(c | d);

// Bitwise XOR
let e = 10;
let f = 5;
console.log(e ^ f);

// Bitwise NOT
let g = 10;
console.log(~g);
    
// Bitwise Left Shift
let h = 10;
console.log(h << 1);
    
// Bitwise Right Shift
let i = 10;
console.log(i >> 1);    